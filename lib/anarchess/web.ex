defmodule Anarchess.Web do
  @moduledoc """
  The Web context.
  """

  import Ecto.Query, warn: false
  alias Anarchess.Repo

  alias Anarchess.Web.Game
  alias Anarchess.Web.Move
  alias Anarchess.Web.Comment

  @doc """
  Returns the list of games.

  ## Examples

      iex> list_games()
      [%Game{}, ...]

  """
  def list_games do
    Repo.all(Game)
  end

  @doc """
  Gets a single game.

  Raises `Ecto.NoResultsError` if the Game does not exist.

  ## Examples

      iex> get_game!(123)
      %Game{}

      iex> get_game!(456)
      ** (Ecto.NoResultsError)

  """
  def get_game!(id), do: Repo.get!(Game, id)

  def get_game_with_moves!(id) do
    Repo.get!(Game, id)
    |> Repo.preload([:moves])
  end

  def get_game_with_comments!(id) do
    Repo.get!(Game, id)
    |> Repo.preload([:comments])
  end

  def get_game_with_all_dependecies!(id) do
    Repo.get!(Game, id)
    |> Repo.preload([:comments, :moves])
  end

  @doc """
  Creates a game.

  ## Examples

      iex> create_game(%{field: value})
      {:ok, %Game{}}

      iex> create_game(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_game(attrs \\ %{}) do
    %Game{}
    |> Game.changeset(attrs)
    |> Repo.insert()
  end

  def create_game_move(game, attrs \\ %{}) do
    game
      |> Ecto.build_assoc(:moves)
      |> Move.changeset(attrs)
      |> Repo.insert
  end

  def create_game_comment(game, attrs \\ %{}) do
    game
      |> Ecto.build_assoc(:comments)
      |> Comment.changeset(attrs)
      |> Repo.insert
  end

  @doc """
  Updates a game.

  ## Examples

      iex> update_game(game, %{field: new_value})
      {:ok, %Game{}}

      iex> update_game(game, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_game(%Game{} = game, attrs) do
    game
    |> Game.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a Game.

  ## Examples

      iex> delete_game(game)
      {:ok, %Game{}}

      iex> delete_game(game)
      {:error, %Ecto.Changeset{}}

  """
  def delete_game(%Game{} = game) do
    Repo.delete(game)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking game changes.

  ## Examples

      iex> change_game(game)
      %Ecto.Changeset{source: %Game{}}

  """
  def change_game(%Game{} = game) do
    Game.changeset(game, %{})
  end

